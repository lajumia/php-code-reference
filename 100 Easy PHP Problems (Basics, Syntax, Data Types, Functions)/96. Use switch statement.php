<?php
$day = "Monday";

switch ($day) {
    case "Monday":
        echo "Start of the week!";
        break;
    case "Tuesday":
        echo "Second day of the week!";
        break;
    case "Wednesday":
        echo "Middle of the week!";
        break;
    case "Thursday":
        echo "Almost there!";
        break;
    case "Friday":
        echo "End of the week!";
        break;
    default:
        echo "It's the weekend!";
}
?>
